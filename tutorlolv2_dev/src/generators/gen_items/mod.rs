macro_rules! decl_items {
    (inner $Name:ident) => {
        pub struct $Name(pub ItemData);

        impl $Name {
            pub fn new(data: ItemData) -> Box<dyn Generator<ItemData>> {
                Box::new(Self(data))
            }

            pub fn end(self) -> MayFail<ItemData> {
                Ok(self.0)
            }

            pub fn into_inner(self) -> ItemData {
                self.0
            }
        }

        impl ::core::ops::Deref for $Name {
            type Target = ItemData;
            fn deref(&self) -> &Self::Target {
                &self.0
            }
        }

        impl ::core::ops::DerefMut for $Name {
            fn deref_mut(&mut self) -> &mut Self::Target {
                &mut self.0
            }
        }
    };
    (
        impl {
            $($Stable:ident),*$(,)*
        },
        default {
            $($Name:ident),*$(,)*
        }
    ) => {
        $(
            decl_items!(inner $Name);

            impl Generator<ItemData> for $Name {
                fn generate(mut self: Box<Self>) -> MayFail<ItemData> {
                    self.infer_stats_ifdef();
                    self.end()
                }
            }
        )*
        $(
            pastey::paste! {
                pub mod [<$Stable:snake>];
            }

            decl_items!(inner $Stable);
        )*

        pub fn item_gen_fn(item_id: &str) -> Option<
            fn(ItemData) -> Box<dyn Generator<ItemData>>
        > {
            match item_id {
                $(stringify!($Name) => Some($Name::new),)*
                $(stringify!($Stable) => Some($Stable::new),)*
                _ => None,
            }
        }

        pub const fn item_gen_names() -> &'static [&'static str] {
            &[
                $(stringify!($Name),)*
                $(stringify!($Stable),)*
            ]
        }

        pub const fn item_gen_riot_ids() -> [u32; item_gen_names().len()] {
            [
                $(ItemId::$Name.to_riot_id(),)*
                $(ItemId::$Stable.to_riot_id(),)*
            ]
        }
    };
}

